import java.math.BigDecimal;

public class Trip {

    private Integer id;

    private String tripId;

    private Integer count;

    private BigDecimal price;

    public Integer getId() {
        return id;
    }

    public String getTripId() {
        return tripId;
    }

    public Integer getCount() {
        return count;
    }

    public BigDecimal getPrice() {
        return price;
    }
}